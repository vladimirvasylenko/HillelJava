package com.hillel.lesson9.classwork;

public class Animal {

    private String name;

    private int age;

// just to remember that constuctor exists by default even if we wont write it in the class
    public Animal() {
    }

    public Animal(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void getAnimalSound() {
        System.out.println("animal sound : Rrrrr");
    }

    @Override
    public String toString() {
        return "Animal{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
